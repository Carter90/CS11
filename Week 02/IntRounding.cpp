#include <cmath>  
#include <iostream>
using namespace std;
int main ()
{
  double x = 2.3;
  //think of the graph of y = x its a line right
  cout << "x:" << x << endl; 
  // what about y = (int)x it's like a stair case
  cout << "(int)x:" << (int)x << endl; 
  // what about y = ceil(x) it's like a stair case but shifted
  cout << "ceil(x):" << ceil(x) << endl; 
  return 0;
}
